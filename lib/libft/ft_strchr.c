#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;

	i = 0;
	if (s == NULL)
		return (NULL);
	while (s[i])
	{
		if (s[i] == (char)c)
			return ((void *)s + i);
		i++;
	}
	if (s[i] == (char)c)
		return ((void *)s + i);
	return (NULL);
}
